def test(d, b):
    g = d
    return b + g


def generate_weigths(nn, a, b):
    weigths = []
    for i in range(1, len(nn)):
        weigths.append([])
        for j in range(len(nn[i]) - 1):
            weigths[i - 1].append([a(0, b) / b for k in nn[i - 1]])
    return weigths


